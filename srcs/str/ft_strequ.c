#include <str.h>

int		ft_strequ(const char *src1, const char *src2)
{
	if (src1 == 0 || src2 == 0)
		return (0);
	while (*src1 == *src2)
	{
		if (*src1 == '\0')
			return (1);
		src1++;
		src2++;
	}
	return (0);
}
